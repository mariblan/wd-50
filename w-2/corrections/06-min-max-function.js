// MIN/MAX
// We have the values: 9,11,55,22
// How do we find the minimum and maximum value?
// Display your result in the console.

// Min => https://developer.mozilla.org/en-US/docs/Web/JavaScript/Reference/Global_Objects/Math/min
// Max => https://developer.mozilla.org/en-US/docs/Web/JavaScript/Reference/Global_Objects/Math/max


const minNum = Math.min(9, 11, 55, 22);
const maxNum = Math.max(9, 11, 55, 22);

console.log('Minimum number of 9, 11, 55 and 22: ', minNum);
console.log('Maximum number of 9, 11, 55 and 22: ', maxNum);

